package day12;

public class Switch {

	public static void main(String[] args) {
		
		int alb = 5;
		
		switch(alb)
		{
		case 1:
			System.out.println("1111");
		case 5:
			System.out.println("5555");
			alb = 7;
			break;
		case 6:
			System.out.println("sdkjhjakd");
			break;
		case 8:
			System.out.println("Bajuuuuuuuuuuu");
			break;
		case 7:
			System.out.println("Tu ka ndrru stu ka ndrru gruja");
			break;
		
			
		default:
			System.out.println("Ti je njeri i keq");
			
		}

	}

}
